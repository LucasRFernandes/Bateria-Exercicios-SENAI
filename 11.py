"""
11) Faça um algoritmo que leia dois valores (considerando que não serão
lidos valores iguais) e os exiba em ordem crescente.

"""


valor1 = int(input("Informe o valor 1: "))
valor2 = int(input("Informe o valor 2: "))


if valor1 > valor2:
    print(valor1)
    print(valor2)


if valor2 > valor1:
    print(valor2)
    print(valor1)
